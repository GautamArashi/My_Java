public class Datatypes {
    public static void main(String[] args)  {
        int x = 10;
        double y = 10.5;
        char z = 'a';
        boolean b = true;
        // long
        // double
        short n = -2765;
        System.out.println(n);
        
        System.out.println("Integer: " + x);
        System.out.println("Double: " + y);
        System.out.println("Character: " + z);
        System.out.println("Boolean: " + b);
    
}
}
